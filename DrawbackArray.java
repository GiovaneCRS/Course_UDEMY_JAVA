public class DrawbackArray {
    public static void main(String[] args)
    {
        int nums[] = new int [6];
        
        nums[0] = 4;
        nums[1] = 2;
        nums[2] = 6;
        nums[3] = 1;
        nums[4] = 8;
        nums[5] = 4;


        for(int i=0;i<=6; i++)
            {
                System.out.println(nums[i]);
            }
            System.out.println();
    }
}
