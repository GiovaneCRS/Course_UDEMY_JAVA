public class forloop {
      
   public static void main(String[] args)
   {
      for(int d = 1; d<=7; d++)
        { 
         System.out.println("Day: " + d);
      
      {
         for(int hr = 1; hr<=25; hr++)
            System.out.println("Hour" + hr);
            
      }
      

      }
         
   }
}